// 1. 引入输入输出流头文件（用于cin/cout输入输出）
#include <iostream>
// 2. 引入vector容器头文件（必须加！用来存图的邻接表）
#include <vector>
// 3. 引入队列头文件（BFS核心数据结构：先进先出队列）
#include <queue>
// 使用标准命名空间，不用每次写std::cin/std::vector
using namespace std;

// 定义最大节点数：100010个节点（满足题目常规数据范围）
const int N = 100010;

// ------------------- 全局变量定义 -------------------
// n：图的总节点数  m：图的总边数
int n, m;
// a、b：临时存储每条边的两个端点
int a, b;
// 【核心】邻接表：存储无向图
// e[x] 是一个数组，存放 节点x 直接相连的所有邻居节点
vector<int> e[N];
// 访问标记数组：vis[i] = 0 表示未访问，=1 表示已访问
int vis[N];
// BFS专用队列：先进先出，存储待遍历的节点
queue<int> q;

// ------------------- BFS核心函数 -------------------
// 参数s：遍历的起始节点（这里我们从节点1开始）
void bfs(int s){
    // 1. 标记起始节点为「已访问」，防止重复入队
    vis[s] = 1;
    // 2. 起始节点入队（队列是BFS的核心，存接下来要遍历的节点）
    q.push(s);

    // 3. 循环：只要队列不为空，就一直遍历
    while (q.size()){
        // 取出队列的「队首元素」（当前要处理的节点）
        int x = q.front();
        // 队首元素出队（已经取出来了，不需要留在队列里）
        // cout << "遍历节点：" << x << endl;
        q.pop();


        // 4. 遍历当前节点x的「所有邻居节点」
        // auto y：自动识别类型，等价于 int y，代表x的邻居
        for (auto y : e[x]){
            // 如果邻居y已经被访问过，直接跳过（避免死循环）
            if (vis[y]) continue;
            // 标记y为已访问
            vis[y] = 1;
            // 邻居y入队，等待后续遍历
            q.push(y);
        }
    }
}

// ------------------- 主函数（程序入口） -------------------
int main(){
    // 输入：n=节点总数，m=边的总数
    cin >> n >> m;

    // 循环m次：输入m条边，构建无向图
    for (int i = 0; i < m; i++){
        // 输入一条边：连接节点a和节点b
        cin >> a >> b;
        // 无向图！a能到b，b也能到a，所以双向添加
        e[a].push_back(b);  // 把b加入a的邻居列表
        e[b].push_back(a);  // 把a加入b的邻居列表
    }

    // 调用BFS函数：从节点1开始遍历整个图
    bfs(1);

    return 0;
}